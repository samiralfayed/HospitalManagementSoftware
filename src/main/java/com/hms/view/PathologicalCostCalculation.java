    private void btnTotalCostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalCostActionPerformed
        // TODO add your handling code here:
        try {
            double costOfSingleTest = Double.parseDouble(txtCostOfSingleTest.getText());
            int numberOfTests = Integer.parseInt(txtNumberOfTests.getText());

            double totalCost = costOfSingleTest * numberOfTests;
            txtTotalCost.setText(String.format("%.2f", totalCost));

            // Display the result in a dialog box
            javax.swing.JOptionPane.showMessageDialog(this, "Total Cost: $" + String.format("%.2f", totalCost));
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please ensure all inputs are valid numbers.");
        }
    }//GEN-LAST:event_btnTotalCostActionPerformed

    private void btnDiscountedCostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiscountedCostActionPerformed
        // TODO add your handling code here:
        try {
            double costOfSingleTest = Double.parseDouble(txtCostOfSingleTest.getText());
            int numberOfTests = Integer.parseInt(txtNumberOfTests.getText());
            double discountPercentage = Double.parseDouble(txtDiscountPercentage.getText());

            double totalCost = costOfSingleTest * numberOfTests;
            double discountAmount = totalCost * (discountPercentage / 100);
            double discountedCost = totalCost - discountAmount;
            txtTotalCost.setText(String.format("%.2f", discountedCost));

            // Display the result in a dialog box
            javax.swing.JOptionPane.showMessageDialog(this, "Discounted Cost: $" + String.format("%.2f", discountedCost));
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please ensure all inputs are valid numbers.");
        }
    }//GEN-LAST:event_btnDiscountedCostActionPerformed

