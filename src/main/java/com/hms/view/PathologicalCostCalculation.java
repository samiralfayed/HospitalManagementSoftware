    private void txtDiscountPercentageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDiscountPercentageActionPerformed
        // TODO add your handling code here:
        String input = txtDiscountPercentage.getText();
        if (!input.matches("\\d+(\\.\\d{1,2})?") || Double.parseDouble(input) > 100) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid discount percentage (0-100).");
            txtDiscountPercentage.setText("");
        }
    }//GEN-LAST:event_txtDiscountPercentageActionPerformed

